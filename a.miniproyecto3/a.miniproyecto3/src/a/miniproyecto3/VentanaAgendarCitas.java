    public CitaAgendada DatosGuardados;
    
    private void ButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGuardarActionPerformed
        //Aca se guardan los datos ingresados

        int Ndocumento;
        int CitaTelefono;
        int CitaFecha;
        int CitaHora;

        String CitaGenero = "";
        String CitaConsultorio = "";
        String CitaHorario = "";
        String ServicioMedico = "";

        Ndocumento = Integer.parseInt(ID.getText());
        CitaFecha = Integer.parseInt(TFielFechaCita.getText());
        CitaHora = Integer.parseInt(hora.getText());

        CitaGenero = BoxGenero.getSelectedItem().toString();
        CitaConsultorio = BoxConsultorio.getSelectedItem().toString();
        CitaHorario = BoxHorario.getSelectedItem().toString();
        ServicioMedico = BoxServicio.getSelectedItem().toString();

        if (!ValidarUsuario(ID.getText())) {
            JOptionPane.showMessageDialog(rootPane, "DEBE AFILIARSE PRIMERO");
            return;
        }

        DatosGuardados = new CitaAgendada(
                Ndocumento, 0, CitaFecha, CitaHora,
                "", "",
                CitaGenero, CitaConsultorio,
                ServicioMedico
        );

        this.almacenamiento.getCitas().add(DatosGuardados);
        Citastxt citastxt = new Citastxt();
        citastxt.crearArchivo();
        citastxt.EscribirArchivo(DatosGuardados);
        JOptionPane.showMessageDialog(rootPane, "SE HA GUARDADO CORRECTAMENTE");
        Limpiar();
    }//GEN-LAST:event_ButtonGuardarActionPerformed

