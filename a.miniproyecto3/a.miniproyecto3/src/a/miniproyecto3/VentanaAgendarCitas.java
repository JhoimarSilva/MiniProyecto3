    public boolean ValidarUsuario(String Id) {
        boolean i = false;
        try {
            FileReader fileReaderLeer = new FileReader("Datos.txt");
            BufferedReader escritura = new BufferedReader(fileReaderLeer);
            String posicion = "";
            while ((posicion = escritura.readLine()) != null) {
                if (posicion.indexOf(Id) != -1) {
                    System.out.println("Se ecnontro el registro" + posicion);
                    i = true;
                }
            }
        } catch (IOException e) {
            System.out.println(e);
        }
        return i;
    }

